  /**
   * Country code
   * @var string
   */
  public $country = '';

  /**
   * First Name: REQUIRED
   * @var string
   */
  public $firstName = '';

  /**
   * Last Name: OPTIONAL
   * @var string
   */
  public $lastName = '';

  #===================================================================

  public function getGender() {

    if (empty($this->firstName)) {
      throw new Exception('Property firstName cannot be empty');
    }

    if (!extension_loaded('Gender')) {
      throw new Exception('PHP extension Gender is not loaded');
    }

    $this->firstName = mb_convert_case($this->firstName, MB_CASE_LOWER, "UTF-8");
    $this->lastName  = mb_convert_case($this->lastName,  MB_CASE_LOWER, "UTF-8");

    $this->country   = strtolower($this->country);

    $alias = array(
      'gb' => 1,
      'ie' => 2,
      'us' => 3,
      'es' => 4,
      'pt' => 5,
      'it' => 6,
      'mt' => 7,
      'fr' => 8,
      'be' => 9,
      'lu' => 10,
      'nl' => 11,
      'de' => 12,
      'at' => 14,
      'ch' => 15,
      'is' => 16,
      'dk' => 17,
      'no' => 18,
      'se' => 19,
      'fi' => 20,
      'ee' => 21,
      'lv' => 22,
      'lt' => 23,
      'pl' => 24,
      'cz' => 25,
      'sk' => 26,
      'hu' => 27,
      'ro' => 28,
      'bg' => 29,
      'ba' => 30,
      'hr' => 31,
      'mk' => 33,
      'me' => 34,
      'rs' => 35,
      'si' => 36,
      'al' => 37,
      'gr' => 38,
      'ru' => 39,
      'by' => 40,
      'md' => 41,
      'ua' => 42,
      'am' => 43,
      'az' => 44,
      'ge' => 45,
      'kz' => 46,
      'uz' => 46,
      'tr' => 47,
      'sa' => 48,
      'il' => 49,
      'cn' => 50,
      'in' => 51,
      'jp' => 52,
      'kr' => 53,
    );

    if (array_key_exists($this->country, $alias)) {
      $countryNum = $alias[$this->country];
    }
    else {
      $countryNum = 0;
    }

    $genObj = new Gender;
    $result = $genObj->get($this->firstName, $countryNum);

    switch ($result) {
      case Gender::IS_FEMALE:
        return 'F';
      case Gender::IS_MOSTLY_FEMALE:
        return 'F';
      case Gender::IS_MALE:
        return 'M';
      case Gender::IS_MOSTLY_MALE:
        return 'M';
    }

    #############################################################

    if (preg_match('/\S+á$/', $this->lastName)) {
      return 'F';
    }

    # Borrowed from <https://github.com/petewarden>
    if (preg_match('/^joh?n/', $this->firstName)) return 'M';
    if (preg_match('/^th?o(m|b)/', $this->firstName)) return 'M';
    if (preg_match('/^frank/', $this->firstName)) return 'M';
    if (preg_match('/^bil/', $this->firstName)) return 'M';
    if (preg_match('/^hans/', $this->firstName)) return 'M';
    if (preg_match('/^ron/', $this->firstName)) return 'M';
    if (preg_match('/^ro(z|s)/', $this->firstName)) return 'F';
    if (preg_match('/^walt/', $this->firstName)) return 'M';
    if (preg_match('/^krishna/', $this->firstName)) return 'M';
    if (preg_match('/^tri(c|sh)/', $this->firstName)) return 'F';
    if (preg_match('/^pas(c|qu)al$/', $this->firstName)) return 'M';
    if (preg_match('/^ellie/', $this->firstName)) return 'F';
    if (preg_match('/^anfernee/', $this->firstName)) return 'M';
    # most names ending in a/e/i/y are female
    if (preg_match('/^.*[aeiy]$/', $this->firstName)) return 'F';
    # allison and variations
    if (preg_match('/^all?[iy]((ss?)|z)on$/', $this->firstName)) return 'F';
    # cathleen, eileen, maureen
    if (preg_match('/een$/', $this->firstName)) return 'F';
    # barry, larry, perry
    if (preg_match('/^[^s].*r[rv]e?y?$/', $this->firstName)) return 'M';
    # clive, dave, steve
    if (preg_match('/^[^g].*v[ei]$/', $this->firstName)) return 'M';
    # carolyn, gwendolyn, vivian
    if (preg_match('/^[^bd].*(b[iy]|y|via)nn?$/', $this->firstName)) return 'F';
    # dewey, stanley, wesley
    if (preg_match('/^[^ajklmnp][^o][^eit]*([glrsw]ey|lie)$/', $this->firstName)) return 'M';
    # heather, ruth, velvet
    if (preg_match('/^[^gksw].*(th|lv)(e[rt])?$/', $this->firstName)) return 'F';
    # gregory, jeremy, zachary
    if (preg_match('/^[cgjwz][^o][^dnt]*y$/', $this->firstName)) return 'M';
    # leroy, murray, roy
    if (preg_match('/^.*[rlr][abo]y$/', $this->firstName)) return 'M';
    # abigail, jill, lillian
    if (preg_match('/^[aehjl].*il.*$/', $this->firstName)) return 'F';
    # duane, eugene, rene
    if (preg_match('/^.*[grguw][ae]y?ne$/', $this->firstName)) return 'M';
    # fleur, lauren, muriel
    if (preg_match('/^[flm].*ur(.*[^eotuy])?$/', $this->firstName)) return 'F';
    # lance, quincy, vince
    if (preg_match('/^[clmqtv].*[^dl][in]c.*[ey]$/', $this->firstName)) return 'M';
    # margaret, marylou, miri;
    if (preg_match('/^m[aei]r[^tv].*([^cklnos]|([^o]n))$/', $this->firstName)) return 'F';
    # clyde, kyle, pascale
    if (preg_match('/^.*[ay][dl]e$/', $this->firstName)) return 'M';
    # blake, luke, mi;
    if (preg_match('/^[^o]*ke$/', $this->firstName)) return 'M';
    # carol, karen, shar;
    if (preg_match('/^[cks]h?(ar[^lst]|ry).+$/', $this->firstName)) return 'F';
    # pam, pearl, rachel
    if (preg_match('/^[pr]e?a([^dfju]|qu)*[lm]$/', $this->firstName)) return 'F';
    # annacarol, leann, ruthann
    if (preg_match('/^.*[aa]nn.*$/', $this->firstName)) return 'F';
    # deborah, leah, sarah
    if (preg_match('/^.*[^cio]ag?h$/', $this->firstName)) return 'F';
    # frances, megan, susan
    if (preg_match('/^[^ek].*[grsz]h?an(ces)?$/', $this->firstName)) return 'F';
    # ethel, helen, gretchen
    if (preg_match('/^[^p]*([hh]e|[ee][lt])[^s]*[ey].*[^t]$/', $this->firstName)) return 'F';
    # george, joshua, theodore
    if (preg_match('/^[^el].*o(rg?|sh?)?(e|ua)$/', $this->firstName)) return 'M';
    # delores, doris, precious
    if (preg_match('/^[dp][eo]?[lr].*s$/', $this->firstName)) return 'F';
    # anthony, henry, rodney
    if (preg_match('/^[^jpswz].*[denor]n.*y$/', $this->firstName)) return 'M';
    # karin, kim, kristin
    if (preg_match('/^k[^v]*i.*[mns]$/', $this->firstName)) return 'F';
    # bradley, brady, bruce
    if (preg_match('/^br[aou][cd].*[ey]$/', $this->firstName)) return 'M';
    # agnes, alexis, glynis
    if (preg_match('/^[acgk].*[deinx][^aor]s$/', $this->firstName)) return 'F';
    # ignace, lee, wallace
    if (preg_match('/^[ilw][aeg][^ir]*e$/', $this->firstName)) return 'M';
    # juliet, mildred, millicent
    if (preg_match('/^[^agw][iu][gl].*[drt]$/', $this->firstName)) return 'F';
    # ari, bela, ira
    if (preg_match('/^[abeiuy][euz]?[blr][aeiy]$/', $this->firstName)) return 'M';
    # iris, lois, phyllis
    if (preg_match('/^[egilp][^eu]*i[ds]$/', $this->firstName)) return 'F';
    # randy, timothy, tony
    if (preg_match('/^[art][^r]*[dhn]e?y$/', $this->firstName)) return 'M';
    # beatriz, bridget, harriet
    if (preg_match('/^[bhl].*i.*[rtxz]$/', $this->firstName)) return 'F';
    # antoine, jerome, tyrone
    if (preg_match('/^.*oi?[mn]e$/', $this->firstName)) return 'M';
    # danny, demetri, dondi
    if (preg_match('/^d.*[mnw].*[iy]$/', $this->firstName)) return 'M';
    # pete, serge, shane
    if (preg_match('/^[^bg](e[rst]|ha)[^il]*e$/', $this->firstName)) return 'M';
    # angel, gail, isabel
    if (preg_match('/^[adfgim][^r]*([bg]e[lr]|il|wn)$/', $this->firstName)) return 'F';
    #------------------------------
    if (in_array($this->firstName, $this->male)) return 'M';
    #------------------------------
    if (in_array($this->firstName, $this->female)) return 'F';
    #---- End of borrowed code ----
    return 'N';
  }

